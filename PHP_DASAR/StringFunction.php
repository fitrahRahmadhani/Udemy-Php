<?php

// join digunakan untuk menggabungkan
var_dump(join("-", ["Fitrah", "Rahmadhani"]));

//explode digunakan untuk memecah
var_dump(explode(" ", "Fitrah Rahmadhani"));

//strtoupper digunakan untuk mengubah semua huruf menjadi kapital
var_dump(strtoupper("Fitrah"));

//strtolower digunakan untuk mengubah semua huruf menjadi kecil
var_dump(strtolower("Fitrah"));

// trim digunakan untuk menghilangkan spasi
var_dump(trim(" Fitrah "));

// substr digunakan untuk mengambil substring
var_dump(substr("Fitrah Rahmadhani", 0, 3));
